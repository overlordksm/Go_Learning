package main

import "fmt"

func main(){
	fmt.Println("Hello Go, I'm ksm!")

	// \t
	fmt.Println("Hello Go!\t I'm ksm!")

	// \n
	fmt.Println("Hello Go!\n I'm ksm!")

	// \\
	fmt.Println("Hello \\Go\\! I'm ksm!")

	// \"
	fmt.Println("Hello \"Go\"! I'm ksm!")

	// \r
	fmt.Println("Hello Go! \rksm! ")
}